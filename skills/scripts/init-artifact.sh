set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Check if project name is provided
if [ -z "$1" ]; then
    echo -e "${RED}Error: Please provide a project name${NC}"
    echo "Usage: $0 <project-name>"
    exit 1
fi

PROJECT_NAME=$1
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo -e "${BLUE}========================================${NC}"
echo -e "${BLUE}  Web Artifacts Builder - Initializer${NC}"
echo -e "${BLUE}========================================${NC}"
echo ""

# Check Node.js version
echo -e "${YELLOW}Checking Node.js version...${NC}"
NODE_VERSION=$(node -v 2>/dev/null | cut -d'v' -f2 | cut -d'.' -f1)
if [ -z "$NODE_VERSION" ] || [ "$NODE_VERSION" -lt 18 ]; then
    echo -e "${RED}Error: Node.js 18+ is required${NC}"
    echo "Please install Node.js 18 or later from https://nodejs.org/"
    exit 1
fi
echo -e "${GREEN}Node.js v${NODE_VERSION} detected${NC}"

# Check/Install pnpm
echo -e "${YELLOW}Checking pnpm...${NC}"
if ! command -v pnpm &> /dev/null; then
    echo -e "${YELLOW}Installing pnpm...${NC}"
    npm install -g pnpm
fi
echo -e "${GREEN}pnpm is available${NC}"

# Detect OS for sed compatibility
if [[ "$OSTYPE" == "darwin"* ]]; then
    SED_INPLACE="sed -i ''"
else
    SED_INPLACE="sed -i"
fi

# Create Vite project
echo ""
echo -e "${YELLOW}Creating Vite + React + TypeScript project...${NC}"
pnpm create vite@latest "$PROJECT_NAME" --template react-ts

cd "$PROJECT_NAME"

# Install dependencies
echo ""
echo -e "${YELLOW}Installing dependencies...${NC}"
pnpm install

# Install Tailwind CSS
echo -e "${YELLOW}Installing Tailwind CSS...${NC}"
pnpm add -D tailwindcss postcss autoprefixer
pnpm exec tailwindcss init -p

# Install shadcn/ui dependencies
echo -e "${YELLOW}Installing shadcn/ui dependencies...${NC}"
pnpm add tailwindcss-animate class-variance-authority clsx tailwind-merge lucide-react
pnpm add @radix-ui/react-accordion @radix-ui/react-alert-dialog @radix-ui/react-avatar
pnpm add @radix-ui/react-checkbox @radix-ui/react-collapsible @radix-ui/react-context-menu
pnpm add @radix-ui/react-dialog @radix-ui/react-dropdown-menu @radix-ui/react-hover-card
pnpm add @radix-ui/react-label @radix-ui/react-menubar @radix-ui/react-navigation-menu
pnpm add @radix-ui/react-popover @radix-ui/react-progress @radix-ui/react-radio-group
pnpm add @radix-ui/react-scroll-area @radix-ui/react-select @radix-ui/react-separator
pnpm add @radix-ui/react-slider @radix-ui/react-slot @radix-ui/react-switch
pnpm add @radix-ui/react-tabs @radix-ui/react-toast @radix-ui/react-toggle
pnpm add @radix-ui/react-toggle-group @radix-ui/react-tooltip

# Configure Tailwind
echo -e "${YELLOW}Configuring Tailwind CSS...${NC}"
cat > tailwind.config.js << 'EOF'
